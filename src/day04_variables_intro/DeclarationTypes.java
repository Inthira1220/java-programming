package day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args){
        int bananas = 34;
        System.out.println(bananas);
        int students = 400;
        System.out.println(students);
        students = 401;
        System.out.println(students);
        int peopleInCity, birdOnTree, fishInLake;
        peopleInCity = 1000;
        birdOnTree = 21;
        fishInLake = 100;
        System.out.println(peopleInCity);
        System.out.println(birdOnTree);
        System.out.println(fishInLake);//err instead of out will change to red color.
        int studentInNY = 58, studentInVa = 200; //
        System.out.println(studentInNY);
        System.out.println(studentInVa);

    }

}
