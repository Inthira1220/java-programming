package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args){ //declare variable n that can store
        int n; //Data type and variable name
        n = 10; //assign/put value 10 in n
        int apples;
        int bananas;
        apples = 25;
        bananas = 100;
        System.out.println(n); // 10
        System.out.println("n"); // n
        System.out.println(apples);//25
        System.out.println(n+apples+bananas);// 10+25+100=135
        System.out.println("I have\t"+ (apples+bananas) + "\tapples and bananas"); //i have 125 apples ans bananas
    }
}
