package ReplitString;

public class DuplicateIt {
    public static void main(String[] args) {
        /**
         * You have 2 words
         * Print the first word, second word, second word, first word
         * Input:
         * one
         * two
         */
        String n = "inthira";
        String m = "Kraithap";
        System.out.println(n+m+n+m);
    }
}
