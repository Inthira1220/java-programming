package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args){
        String brand = "Rolex";
        String color = "Black";
        double price = 100000.99;
        boolean waterResistant = true;
        boolean isSmart = true;
        char gender = 'U';
        System.out.println("Brand of Watch:"+"\t\t\t"+brand);
        System.out.println("Watch's Color:"+"\t\t\t"+color);
        System.out.println("Price:"+"\t\t\t\t\t"+"$"+price);
        System.out.println("Water Resistant:"+"\t\t"+waterResistant);
        System.out.println("Smart Watch:"+"\t\t\t"+isSmart);
        System.out.println("Gender:"+"\t\t\t\t\t"+gender);

    }
}
