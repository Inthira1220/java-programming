package day05_primitives_concatanation;

public class JobInfo {
    public static void main(String[] arg){
        String title = "Senior";
        String company = "Apple";
        String jobDescription = "Coding Developer";
        double salary = 120000.00;
        int yearsExp = 3;
        boolean hasBenefits = true;
        System.out.println("Title:"+"\t\t\t\t"+title);
        System.out.println("Company: "+"\t\t\t"+company);
        System.out.println("jobDescription:"+"\t\t"+jobDescription);
        System.out.println("Salary:"+"\t\t\t\t"+"$"+salary);
        System.out.println("yearsExp:"+"\t\t\t"+yearsExp);
        System.out.println("Does Company has Benefits:"+"\t"+hasBenefits);
        System.out.println();
        System.out.println("Title:"+title+"\tCompany:"+company+"\tJobDescription:"+jobDescription+"\tSalary:"+salary+"\tYearsExperience:"+yearsExp);
    }
}


