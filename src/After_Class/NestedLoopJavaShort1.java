package After_Class;

public class NestedLoopJavaShort1 {
    public static void main(String[] args) {
        //a nested for loop
        for (int r = 0; r < 2; r++) {
            for (int k = 0; k < 3; k++) {
                System.out.println("IRow" + r + "-Column" + k + "l");
            }
            System.out.println();
        }
    }
}
