package day06_arithmetic_operators;

public class ArithmeticOperator {
    public static void main(String[] args){
        System.out.println(5 + 10);//space or without space is no matter
        System.out.println(10-3);//7
        System.out.println(3*3);//9
        System.out.println(15/5);//3
        System.out.println(100 % 9);//1, whatever left over!!
        System.out.println(13 % 5);//3
        System.out.println(-2*2);//-4

        int chairs = 4+2;
        System.out.println(chairs);
        int spoons = 10 + 20 + 12 +0;
        System.out.println("Answer from input line 16 is "+spoons);
        System.out.println("There are "+spoons+ " spoons in the kitchen.");
    }
}
